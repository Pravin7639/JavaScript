
function changeHeading() {
    var heading = document.getElementById('head');
    console.log(`Heading : ${heading}`);

    if (heading.innerText == 'Change Text') {
        heading.innerText = "Student Heading";
    } else {
        heading.innerText = "Change Text";
    }
}



function changeName() {
    var name = document.getElementById('pm');
    if (name.innerText == 'Pravin Mane') {
        name.innerText = "Pawan Mane";
    } else {
        name.innerText = "Pravin Mane";
    }

}























